    # -- Section 11: Clinical Summary for MD -------------------------------

    def _section_11_clinical_summary(self, analysis: AnalysisResult) -> str:
        lines = [
            "## 11. Clinical Summary for Healthcare Provider",
            "",
        ]

        profile = analysis.patient_profile
        ba = analysis.biological_age

        lines.append(
            f"**Patient:** {profile.patient_id}, {profile.age}yo {profile.sex}"
        )
        lines.append(
            f"**Biological Age:** {ba.biological_age:.1f} years "
            f"(acceleration: {ba.age_acceleration:+.1f} years)"
        )
        lines.append("")

        # Key findings summary
        lines.append("**Key Findings:**")
        lines.append("")

        # Critical alerts
        if analysis.critical_alerts:
            for alert in analysis.critical_alerts:
                lines.append(f"- {alert}")
            lines.append("")

        # Disease risks
        risk_diseases = [
            t for t in analysis.disease_trajectories
            if t.risk_level in (RiskLevel.HIGH, RiskLevel.MODERATE, RiskLevel.CRITICAL)
        ]
        if risk_diseases:
            lines.append("**Elevated Disease Risks:**")
            for traj in risk_diseases:
                lines.append(
                    f"- {traj.disease.value}: {traj.risk_level.value}"
                    + (f" (est. {traj.years_to_onset_estimate:.0f}y to onset)"
                       if traj.years_to_onset_estimate else "")
                )
            lines.append("")

        # PGx alerts
        alert_pgx = [
            p for p in analysis.pgx_results
            if p.phenotype.value in ("poor", "ultra_rapid")
        ]
        if alert_pgx:
            lines.append("**Pharmacogenomic Alerts:**")
            for pgx in alert_pgx:
                lines.append(
                    f"- {pgx.gene} {pgx.star_alleles}: {pgx.phenotype.value} metabolizer"
                )
            lines.append("")

        # Recommended actions
        lines.append("**Recommended Actions:**")
        lines.append("1. Review critical alerts and PGx findings")
        lines.append("2. Implement prioritized interventions (Section 8)")
        lines.append("3. Order follow-up labs per monitoring schedule (Section 9)")
        lines.append("4. Discuss supplement protocol with patient (Section 10)")

        return "\n".join(lines)

