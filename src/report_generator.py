"""Precision Biomarker Agent -- 12-Section Patient Report Generator.

Takes an AnalysisResult and generates a structured clinical report in
markdown format covering biological age, disease trajectories,
pharmacogenomics, nutritional analysis, and actionable recommendations.

Sections:
 1. Biological Age Assessment
 2. Executive Findings (top 5 critical/high priority)
 3. Biomarker-Gene Correlation Map
 4. Disease Trajectory Analysis (6 diseases)
 5. Pharmacogenomic Profile
 6. Nutritional Analysis
 7. Interconnected Pathways
 8. Prioritized Action Plan
 9. Monitoring Schedule
10. Supplement Protocol Summary
11. Clinical Summary for MD
12. References

Author: Adam Jones
Date: March 2026
"""

from datetime import datetime, timezone
from typing import Dict, List, Optional

from .models import (
    AnalysisResult,
    DiseaseTrajectoryResult,
    GenotypeAdjustmentResult,
    PatientProfile,
    PGxResult,
    RiskLevel,
)

VERSION = "1.0.0"


class ReportGenerator:
    """Generates structured 12-section precision biomarker reports.

    Usage:
        generator = ReportGenerator()
        markdown = generator.generate(analysis_result, patient_profile)
    """

    def generate(self, analysis: AnalysisResult,
                 patient_profile: Optional[PatientProfile] = None) -> str:
        """Generate a complete 12-section patient report in markdown.

        Args:
            analysis: AnalysisResult from PrecisionBiomarkerAgent.analyze_patient().
            patient_profile: Optional override; defaults to analysis.patient_profile.

        Returns:
            Complete markdown report string.
        """
        profile = patient_profile or analysis.patient_profile
        timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")

        sections = [
            self._header(profile, timestamp),
            self._section_1_biological_age(analysis),
            self._section_2_executive_findings(analysis),
            self._section_3_biomarker_gene_map(analysis),
            self._section_4_disease_trajectories(analysis),
            self._section_5_pharmacogenomic_profile(analysis),
            self._section_6_nutritional_analysis(analysis, profile),
            self._section_7_interconnected_pathways(analysis),
            self._section_8_action_plan(analysis),
            self._section_9_monitoring_schedule(analysis),
            self._section_10_supplement_protocol(analysis, profile),
            self._section_11_clinical_summary(analysis),
            self._section_12_references(),
            self._section_13_clinical_validation(analysis),
            self._footer(timestamp),
        ]

        return "\n\n".join(sections)

    # -- Header & Footer ---------------------------------------------------

    def _header(self, profile: PatientProfile, timestamp: str) -> str:
        return (
            f"# Precision Biomarker Intelligence Report\n\n"
            f"**Patient ID:** {profile.patient_id}\n"
            f"**Age:** {profile.age} | **Sex:** {profile.sex}\n"
            f"**Generated:** {timestamp}\n"
            f"**Report Version:** {VERSION}\n\n"
            f"---"
        )

    def _footer(self, timestamp: str) -> str:
        from .knowledge import KNOWLEDGE_VERSION

        lines = [
            "---",
            "",
            f"*Knowledge base v{KNOWLEDGE_VERSION['version']} "
            f"(updated {KNOWLEDGE_VERSION['last_updated']}). "
            f"CPIC guidelines: {KNOWLEDGE_VERSION['cpic_version']}. "
            f"ADA Standards: {KNOWLEDGE_VERSION['ada_standards']}.*",
            "",
            f"*Generated by HCLS AI Factory -- Precision Biomarker Intelligence Agent v{VERSION}*",
            f"*Report generated {timestamp}. This report is for informational purposes and should "
            f"be reviewed by a qualified healthcare provider before making clinical decisions.*",
        ]
        return "\n".join(lines)

    # -- Section 1: Biological Age -----------------------------------------

    def _section_1_biological_age(self, analysis: AnalysisResult) -> str:
        ba = analysis.biological_age
        lines = [
            "## 1. Biological Age Assessment",
            "",
            "| Metric | Value |",
            "|--------|-------|",
            f"| Chronological Age | {ba.chronological_age} years |",
            f"| Biological Age (PhenoAge) | {ba.biological_age:.1f} years |",
            f"| Age Acceleration | {ba.age_acceleration:+.1f} years |",
        ]
        if ba.grimage_score is not None:
            lines.append(f"| GrimAge Surrogate Score | {ba.grimage_score:.1f} years |")
        lines.append(f"| Mortality Risk Score | {ba.mortality_risk:.4f} |")

        # Confidence interval display
        ci = ba.confidence_interval if ba.confidence_interval else None
        if ci:
            lines.append(
                f"| 95% Confidence Interval | {ci.get('lower', '?')} - {ci.get('upper', '?')} years |"
            )
            lines.append(f"| Data Completeness | {ci.get('note', 'N/A')} |")
        if ba.risk_confidence:
            lines.append(f"| Risk Assessment Confidence | {ba.risk_confidence} |")
        lines.append("")

        # Interpretation
        if ba.age_acceleration > 5:
            lines.append(
                "**Interpretation:** Significantly accelerated aging detected. "
                "Biological age exceeds chronological age by more than 5 years, "
                "indicating elevated all-cause mortality risk and potential for "
                "age-related disease."
            )
        elif ba.age_acceleration > 2:
            lines.append(
                "**Interpretation:** Moderately accelerated aging. Targeted interventions "
                "on modifiable PhenoAge drivers (glucose, inflammation, RDW) are recommended."
            )
        elif ba.age_acceleration > -2:
            lines.append(
                "**Interpretation:** Biological age is within normal range of chronological age. "
                "Continue current health maintenance."
            )
        else:
            lines.append(
                "**Interpretation:** Biological age is younger than chronological age, indicating "
                "favorable metabolic resilience and slower aging trajectory."
            )

        # Top aging drivers
        if ba.aging_drivers:
            lines.append("")
            lines.append("**Top Aging Drivers:**")
            lines.append("")
            lines.append("| Biomarker | Value | Direction | Contribution |")
            lines.append("|-----------|-------|-----------|--------------|")
            for driver in ba.aging_drivers[:5]:
                lines.append(
                    f"| {driver.get('biomarker', '')} | "
                    f"{driver.get('value', '')} | "
                    f"{driver.get('direction', '')} | "
                    f"{driver.get('contribution', 0):.4f} |"
                )

        return "\n".join(lines)

    # -- Section 2: Executive Findings -------------------------------------

    def _section_2_executive_findings(self, analysis: AnalysisResult) -> str:
        lines = [
            "## 2. Executive Findings",
            "",
            "**Top priority findings requiring attention:**",
            "",
        ]

        findings = []

        # Critical alerts first
        for alert in analysis.critical_alerts:
            findings.append(("CRITICAL", alert))

        # High-risk disease trajectories
        for traj in analysis.disease_trajectories:
            if traj.risk_level == RiskLevel.HIGH:
                findings.append((
                    "HIGH",
                    f"{traj.disease.value}: {traj.risk_level.value} risk. "
                    + (f"Estimated {traj.years_to_onset_estimate:.0f} years to onset. "
                       if traj.years_to_onset_estimate else "")
                    + f"Interventions: {', '.join(traj.intervention_recommendations[:2])}"
                    if traj.intervention_recommendations else "",
                ))
            elif traj.risk_level == RiskLevel.MODERATE:
                findings.append((
                    "MODERATE",
                    f"{traj.disease.value}: {traj.risk_level.value} risk. "
                    + (f"Monitoring recommended. "
                       f"Interventions: {', '.join(traj.intervention_recommendations[:2])}"
                       if traj.intervention_recommendations else "Monitoring recommended."),
                ))

        # Biological age finding
        if analysis.biological_age.age_acceleration > 2:
            findings.append((
                "HIGH",
                f"Biological age acceleration of {analysis.biological_age.age_acceleration:+.1f} years "
                f"detected. Multi-system aging intervention recommended.",
            ))

        # Sort by priority and take top 5
        priority_order = {"CRITICAL": 0, "HIGH": 1, "MODERATE": 2}
        findings.sort(key=lambda f: priority_order.get(f[0], 3))

        for i, (level, desc) in enumerate(findings[:5], 1):
            emoji_map = {"CRITICAL": "[!!!]", "HIGH": "[!!]", "MODERATE": "[!]"}
            marker = emoji_map.get(level, "[-]")
            lines.append(f"{i}. **{marker} {level}:** {desc}")

        if not findings:
            lines.append("No critical or high-priority findings detected.")

        return "\n".join(lines)

    # -- Section 3: Biomarker-Gene Correlation Map -------------------------

    def _section_3_biomarker_gene_map(self, analysis: AnalysisResult) -> str:
        lines = [
            "## 3. Biomarker-Gene Correlation Map",
            "",
        ]

        if not analysis.genotype_adjustments:
            lines.append("No genotype-biomarker adjustments available for this patient.")
            return "\n".join(lines)

        lines.extend([
            "| Biomarker | Gene | Genotype | Standard Range | Adjusted Range | Rationale |",
            "|-----------|------|----------|----------------|----------------|-----------|",
        ])

        for adj in analysis.genotype_adjustments:
            rationale_short = adj.rationale[:150] + "..." if len(adj.rationale) > 150 else adj.rationale
            lines.append(
                f"| {adj.biomarker} | {adj.gene} | {adj.genotype} | "
                f"{adj.standard_range} | {adj.adjusted_range} | {rationale_short} |"
            )

        return "\n".join(lines)

    # -- Section 4: Disease Trajectories -----------------------------------

    def _section_4_disease_trajectories(self, analysis: AnalysisResult) -> str:
        lines = [
            "## 4. Disease Trajectory Analysis",
            "",
        ]

        if not analysis.disease_trajectories:
            lines.append("No disease trajectory data available.")
            return "\n".join(lines)

        for traj in analysis.disease_trajectories:
            risk_indicator = {
                RiskLevel.CRITICAL: "[!!!]",
                RiskLevel.HIGH: "[!!]",
                RiskLevel.MODERATE: "[!]",
                RiskLevel.LOW: "[-]",
                RiskLevel.NORMAL: "[OK]",
            }.get(traj.risk_level, "[-]")

            lines.append(f"### {traj.disease.value.title()} {risk_indicator}")
            lines.append("")
            lines.append(f"**Risk Level:** {traj.risk_level.value}")

            if traj.years_to_onset_estimate:
                lines.append(
                    f"**Estimated Years to Clinical Onset:** ~{traj.years_to_onset_estimate:.0f} years"
                )

            if traj.current_markers:
                lines.append("")
                lines.append("**Current Biomarkers:**")
                for marker, value in traj.current_markers.items():
                    lines.append(f"- {marker}: {value}")

            if traj.genetic_risk_factors:
                lines.append("")
                lines.append("**Genetic Risk Factors:**")
                for factor in traj.genetic_risk_factors:
                    lines.append(f"- {factor}")

            if traj.intervention_recommendations:
                lines.append("")
                lines.append("**Recommended Interventions:**")
                for rec in traj.intervention_recommendations:
                    lines.append(f"- {rec}")

            lines.append("")

        return "\n".join(lines)

    # -- Section 5: Pharmacogenomic Profile --------------------------------

    def _section_5_pharmacogenomic_profile(self, analysis: AnalysisResult) -> str:
        lines = [
            "## 5. Pharmacogenomic Profile",
            "",
        ]

        if not analysis.pgx_results:
            lines.append("No pharmacogenomic data available for this patient.")
            return "\n".join(lines)

        lines.extend([
            "| Gene | Star Alleles | Phenotype | Alert Level |",
            "|------|-------------|-----------|-------------|",
        ])

        alert_genes = []
        for pgx in analysis.pgx_results:
            phenotype = pgx.phenotype.value.replace("_", " ").title()
            alert = "Standard"
            if pgx.phenotype.value in ("poor", "ultra_rapid"):
                alert = "HIGH"
                alert_genes.append(pgx)
            elif pgx.phenotype.value == "intermediate":
                alert = "MODERATE"
            lines.append(
                f"| {pgx.gene} | {pgx.star_alleles} | {phenotype} | {alert} |"
            )

        # Detailed drug recommendations for alert-level genes
        if alert_genes:
            lines.append("")
            lines.append("### Drug-Specific Recommendations")
            lines.append("")
            for pgx in alert_genes:
                lines.append(f"**{pgx.gene} ({pgx.star_alleles} -- {pgx.phenotype.value})**")
                lines.append("")
                for drug_info in pgx.drugs_affected:
                    drug = drug_info.get("drug", "Unknown")
                    rec = drug_info.get("recommendation", "")
                    cpic = drug_info.get("cpic_level", "")
                    lines.append(f"- **{drug}** (CPIC {cpic}): {rec}")
                lines.append("")

        return "\n".join(lines)

